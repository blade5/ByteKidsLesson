class BasicOperations {
    public static void main(String args[]) {
        int x1 = 8;
        int y1 = 2;
        int plusResult = x1 + y1;
        System.out.println("8 - 2 = " + plusResult);

        int x2 = 8;
        int y2 = 2;
        int minusResult = x2 - y2;
        System.out.println("8 - 2 = " + minusResult);

        int x3 = 8;
        int y3 = 2;
        int multiplyResult = x3 * y3;
        System.out.println("8 * 2 = " + multiplyResult);

        int x4 = 8;
        int y4 = 2;
        int divisionResult = x4 / y4;
        System.out.println("8 / 2 = " + divisionResult);
    }
}
