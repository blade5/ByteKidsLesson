class StringAddOperations {
    public static void main(String args[]) {
       String x1 = "姓名: ";
       String y1 = "小明";
       System.out.println(x1 + y1);

       String x2 = "年龄：";
       int y2 = 12;
       System.out.println(x2 + y2);

       String x3 = "身高：";
       double y3 = 162.5;
       System.out.println(x3 + y3);
    }
}
